/*
Program: Array Initialization and Access
Language: C++
Time Complexity: O(1)
Space Complexity: O(1)
*/

#include <iostream>
using namespace std;

int main()
{
    double marks[5] = {10, 20, 30, 40};

    cout << "\nmarks = " << marks[0];
    cout << "\nmarks = " << marks[1];
    cout << "\nmarks = " << marks[2];
    cout << "\nmarks = " << marks[3];

    return 0;
}
// Output
// marks = 10
// marks = 20
// marks = 30
// marks = 40
